def fix_problematic_columns(df):
    """Naprawia problematyczne kolumny w DataFrame"""
    # Lista znanych problematycznych kolumn
    problematic_columns = ['Street Num', 'Numer', 'Postal', 'Exception',
                           'OPLD Consignee Name', 'Consignee Name', 'Consignee']

    # Sprawd≈∫ wszystkie kolumny pod kƒÖtem mieszanych typ√≥w
    for col in df.columns:
        try:
            # Sprawd≈∫ czy kolumna ma mieszane typy danych
            if df[col].dtype == 'object':
                # Sprawd≈∫ czy sƒÖ r√≥≈ºne typy w kolumnie
                non_null_values = df[col].dropna()
                if len(non_null_values) > 0:
                    types_in_col = non_null_values.apply(type).unique()
                    if len(types_in_col) > 1:
                        # Konwertuj wszystko na string
                        df[col] = df[col].astype(str)
        except Exception:
            # Je≈õli nie mo≈ºna sprawdziƒá typ√≥w, po prostu konwertuj na string
            try:
                df[col] = df[col].astype(str)
            except Exception:
                pass

    # Konwertuj znane problematyczne kolumny
    for col in problematic_columns:
        if col in df.columns:
            try:
                df[col] = df[col].astype(str)
            except Exception:
                pass

    return df


def extract_driver_name(driver_id):
    """WyciƒÖga czƒô≈õƒá nazwy Driver ID od 6 do 8 znaku"""
    driver_str = str(driver_id)
    if len(driver_str) >= 8:
        return driver_str[5:8]  # od 6 do 8 znaku (indeksy 5-7)
    elif len(driver_str) >= 5:
        return driver_str[5:]   # od 6 znaku do ko≈Ñca
    else:
        return driver_str       # ca≈Ça nazwa je≈õli kr√≥tsza ni≈º 5 znak√≥w


def create_gps_map(df):
    """Tworzy mapƒô z punktami GPS na podstawie kolumn GPSX i GPSY"""
    # Sprawd≈∫ czy istniejƒÖ kolumny GPS
    if 'GPSX' not in df.columns or 'GPSY' not in df.columns:
        return None

    # Filtruj dane z prawid≈Çowymi wsp√≥≈Çrzƒôdnymi GPS
    gps_data = df[(df['GPSX'].notna()) & (df['GPSY'].notna()) &
                  (df['GPSX'] != '') & (df['GPSY'] != '')]

    if len(gps_data) == 0:
        return None

    # Konwertuj wsp√≥≈Çrzƒôdne na liczby
    try:
        gps_data = gps_data.copy()
        gps_data['GPSX'] = pd.to_numeric(gps_data['GPSX'], errors='coerce')
        gps_data['GPSY'] = pd.to_numeric(gps_data['GPSY'], errors='coerce')

        # Usu≈Ñ wiersze z nieprawid≈Çowymi wsp√≥≈Çrzƒôdnymi
        gps_data = gps_data.dropna(subset=['GPSX', 'GPSY'])

        if len(gps_data) == 0:
            return None

    except Exception as e:
        st.warning(f"‚ö†Ô∏è B≈ÇƒÖd podczas konwersji wsp√≥≈Çrzƒôdnych GPS: {str(e)}")
        return None

    # Sprawd≈∫ czy wsp√≥≈Çrzƒôdne wyglƒÖdajƒÖ jak UTM (du≈ºe liczby) czy geograficzne (ma≈Çe liczby)
    sample_x = gps_data['GPSX'].iloc[0] if len(gps_data) > 0 else 0
    sample_y = gps_data['GPSY'].iloc[0] if len(gps_data) > 0 else 0

    # Sprawd≈∫ czy GPSX to szeroko≈õƒá (latitude) czy d≈Çugo≈õƒá (longitude)
    # Je≈õli GPSX jest w zakresie 49-55, to prawdopodobnie to jest latitude (szeroko≈õƒá)
    # Je≈õli GPSX jest w zakresie 14-24, to prawdopodobnie to jest longitude (d≈Çugo≈õƒá)
    is_gpsx_latitude = (sample_x >= 49 and sample_x <= 55)
    is_gpsx_longitude = (sample_x >= 14 and sample_x <= 24)

    # Je≈õli wsp√≥≈Çrzƒôdne sƒÖ du≈ºe (prawdopodobnie UTM), skonwertuj je
    if abs(sample_x) > 180 or abs(sample_y) > 90:
        st.info("üîÑ Wykryto wsp√≥≈Çrzƒôdne UTM - konwertujƒô na wsp√≥≈Çrzƒôdne geograficzne...")

        # Konwersja UTM na geograficzne (przybli≈ºona dla Polski)
        # U≈ºyj przybli≈ºonej konwersji UTM (bez pyproj dla kompatybilno≈õci z Streamlit Cloud)
        st.info("üîÑ U≈ºywam przybli≈ºonej konwersji UTM ‚Üí geograficzne")

        # Przybli≈ºona konwersja dla Polski (UTM Zone 33N)
        # To jest przybli≈ºone, ale wystarczajƒÖce dla wiƒôkszo≈õci przypadk√≥w w Polsce
        # Przybli≈ºenie dla Polski
        gps_data['longitude'] = (gps_data['GPSX'] - 500000) / 111320 + 15.0
        # Przybli≈ºenie dla Polski
        gps_data['latitude'] = (gps_data['GPSY'] - 5000000) / 110540 + 52.0

    else:
        # Wsp√≥≈Çrzƒôdne ju≈º sƒÖ geograficzne - sprawd≈∫ kolejno≈õƒá
        if is_gpsx_latitude:
            # GPSX to latitude (szeroko≈õƒá), GPSY to longitude (d≈Çugo≈õƒá)
            st.info(
                "üîç Wykryto: GPSX = szeroko≈õƒá geograficzna, GPSY = d≈Çugo≈õƒá geograficzna")
            gps_data['latitude'] = gps_data['GPSX']
            gps_data['longitude'] = gps_data['GPSY']
        elif is_gpsx_longitude:
            # GPSX to longitude (d≈Çugo≈õƒá), GPSY to latitude (szeroko≈õƒá)
            st.info(
                "üîç Wykryto: GPSX = d≈Çugo≈õƒá geograficzna, GPSY = szeroko≈õƒá geograficzna")
            gps_data['longitude'] = gps_data['GPSX']
            gps_data['latitude'] = gps_data['GPSY']
        else:
            # Nie mo≈ºna okre≈õliƒá - u≈ºyj domy≈õlnej kolejno≈õci
            st.warning(
                "‚ö†Ô∏è Nie mo≈ºna okre≈õliƒá kolejno≈õci wsp√≥≈Çrzƒôdnych - u≈ºywam domy≈õlnej kolejno≈õci")
            gps_data['longitude'] = gps_data['GPSX']
            gps_data['latitude'] = gps_data['GPSY']

    # Sprawd≈∫ czy wsp√≥≈Çrzƒôdne sƒÖ w rozsƒÖdnym zakresie dla Polski
    valid_coords = gps_data[
        (gps_data['longitude'] >= 14) & (gps_data['longitude'] <= 24) &
        (gps_data['latitude'] >= 49) & (gps_data['latitude'] <= 55)
    ]

    if len(valid_coords) == 0:
        st.warning(
            "‚ö†Ô∏è Wsp√≥≈Çrzƒôdne nie wyglƒÖdajƒÖ na polskie - sprawd≈∫ format danych")
        # U≈ºyj oryginalnych wsp√≥≈Çrzƒôdnych jako fallback
        gps_data['longitude'] = gps_data['GPSX']
        gps_data['latitude'] = gps_data['GPSY']
    else:
        gps_data = valid_coords

    # Oblicz centrum mapy
    center_lat = gps_data['latitude'].mean()
    center_lon = gps_data['longitude'].mean()

    # Utw√≥rz mapƒô
    m = folium.Map(
        location=[center_lat, center_lon],
        zoom_start=10,
        tiles='OpenStreetMap'
    )

    # Dodaj punkty na mapƒô
    for idx, row in gps_data.iterrows():
        # Przygotuj popup z informacjami
        popup_text = f"""
        <b>Numer monitorowania:</b> {row.get('Numer', 'Brak')}<br>
        <b>Driver ID:</b> {row.get('Driver ID:', 'Brak')}<br>
        <b>Data:</b> {row.get('DATA', 'Brak')}<br>
        <b>Miasto:</b> {row.get('City Name', 'Brak')}<br>
        <b>Exception info:</b> {row.get('Exception info', 'Brak')}<br>
        <b>Wsp√≥≈Çrzƒôdne:</b> {row['latitude']:.6f}, {row['longitude']:.6f}
        """

        # Kolor punktu na podstawie Exception info
        color = 'red'  # domy≈õlnie czerwony
        if 'Exception info' in row and pd.notna(row['Exception info']):
            if 'DR RELEASED' in str(row['Exception info']):
                color = 'green'
            elif 'COMM INS REL' in str(row['Exception info']):
                color = 'blue'
            elif 'SIG OBTAINED' in str(row['Exception info']):
                color = 'orange'

        # Dodaj marker
        folium.CircleMarker(
            location=[row['latitude'], row['longitude']],
            radius=6,
            popup=folium.Popup(popup_text, max_width=300),
            color='black',
            weight=1,
            fillColor=color,
            fillOpacity=0.7
        ).add_to(m)

    return m


# Funkcja do ≈Çadowania pliku Excel
@st.cache_data
def load_excel_file(file):
    """≈Åaduje plik Excel i zwraca s≈Çownik z arkuszami"""
    try:
        # Sprawd≈∫ rozszerzenie pliku
        file_extension = file.name.split('.')[-1].lower()

        if file_extension == 'xlsb':
            # Obs≈Çuga plik√≥w .xlsb
            sheets_dict = {}
            with pyxlsb.open_workbook(file) as wb:
                for sheet_name in wb.sheets:
                    try:
                        dataframe = pd.read_excel(
                            file, sheet_name=sheet_name, engine='pyxlsb')
                        # Napraw problematyczne kolumny
                        dataframe = fix_problematic_columns(dataframe)
                        sheets_dict[sheet_name] = dataframe
                    except Exception as e:
                        st.warning(
                            f"‚ö†Ô∏è Problem z arkuszem {sheet_name}: {str(e)}")
                        # Spr√≥buj za≈Çadowaƒá z domy≈õlnymi ustawieniami
                        try:
                            dataframe = pd.read_excel(
                                file, sheet_name=sheet_name, engine='pyxlsb', dtype=str)
                            sheets_dict[sheet_name] = dataframe
                        except Exception:
                            st.error(
                                f"‚ùå Nie uda≈Ço siƒô za≈Çadowaƒá arkusza {sheet_name}")
            return sheets_dict
        else:
            # Obs≈Çuga plik√≥w .xlsx i .xls
            excel_file = pd.ExcelFile(file)
            sheets_dict = {}

            for sheet_name in excel_file.sheet_names:
                try:
                    dataframe = pd.read_excel(file, sheet_name=sheet_name)
                    # Napraw problematyczne kolumny
                    dataframe = fix_problematic_columns(dataframe)
                    sheets_dict[sheet_name] = dataframe
                except Exception as e:
                    st.warning(f"‚ö†Ô∏è Problem z arkuszem {sheet_name}: {str(e)}")
                    # Spr√≥buj za≈Çadowaƒá z domy≈õlnymi ustawieniami
                    try:
                        dataframe = pd.read_excel(
                            file, sheet_name=sheet_name, dtype=str)
                        sheets_dict[sheet_name] = dataframe
                    except Exception:
                        st.error(
                            f"‚ùå Nie uda≈Ço siƒô za≈Çadowaƒá arkusza {sheet_name}")

            return sheets_dict
    except (ValueError, FileNotFoundError, PermissionError) as e:
        st.error(f"B≈ÇƒÖd podczas ≈Çadowania pliku: {str(e)}")
        return None


